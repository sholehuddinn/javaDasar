package operatorrelasional;
public class OperatorRelasional {
    public static void main(String[] args) {
        
        int a = 42;
        int b = 37;
        int c = 37;

        System.out.println("NILAI VARIABEL");
        System.out.println("a = "+a);
        System.out.println("b = "+b);
        System.out.println("c = "+c);
        System.out.println("");
        System.out.println(">");
        System.out.println("a > b = " + (a>b));
        System.out.println("b > a = " + (b>a));
        System.out.println("c > b = " + (c>b));
        System.out.println("");
        System.out.println(">==");
        System.out.println("a >= b = " + (a>=b));
        System.out.println("b >= a = " + (b>=a));
        System.out.println("b >= b = " + (c>=b));
        System.out.println("");
        System.out.println("<");
        System.out.println("a < b = " + (a<b));
        System.out.println("b < a = " + (b<a));
        System.out.println("c < b = " + (c<b));
        System.out.println("");
        System.out.println("<==");
        System.out.println("a <= b = " + (a<=b));
        System.out.println("b <= a = " + (b<=a));
        System.out.println("c <= b = " + (c<=b));
        System.out.println("");
        System.out.println("==");
        System.out.println("a == b = " + (a==b));
        System.out.println("c == b = " + (c==b));
        System.out.println("");
        System.out.println("!=");
        System.out.println("a != b = " + (a!=b));     //tidak sama dengan
        System.out.println("c != b = " + (c!=b));
        System.out.println("");














    }
    
}
