        printAngka(16.7d);
        printAngka(90);
        
            //BIKIN FUNGSI TUPOK TUMPOK RUMUSNYA JUGA BISA
            //NANTI NET BEANS NYA YANG NYESUAI IN TIPE DATANYA
        
