package looping;
public class Looping {
    public static void main(String[] args) {

        byte x = 5;
        byte y =++x;
        System.out.println("y=" +y);

        byte e =7;
        byte z =--e;
        System.out.println("z=" +z);
        
        byte b =8;
        byte c = b++;
        System.out.println(c);
        
        byte a =3;
        byte d =a++;
        System.out.println(d);
        
        System.out.println("java for looping");
        
        for (int i = 1; i <= 10; i++) {
        System.out.println(i);
        }
        System.out.println("");






    }
    
}
