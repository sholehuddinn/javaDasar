package shortcutoperator;
public class ShortcutOperator {
    public static void main(String[] args) {

        int x =10;
        System.out.println("x = "+x);
        
        x+=4;
        System.out.println("x+=4 -->" +x);
        
        x -=8;
        System.out.println("x-=8 -->"+x);        

        x*=3;
        System.out.println("x*=3 -->"+x);    










    }
    
}
