package loopingyutub;
public class forloop {
    public static void main(String[] args) {
        System.out.println("awal");
        
        for (int i = 0; i < 10; i++) {
            System.out.println("for loop ke-"+i);
        }
         
        System.out.println("tengah");
        
        for (int i = 10; i > 3; i--) {
            System.out.println("for loop ke-"+i);
            
            
        // di cek dulu kondisis nya baru di jalankan program nya

        }
    }
}
